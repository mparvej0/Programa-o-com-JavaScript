//for Loop
for(let count = 1; count <= 5; count++) {
    console.log("Hello World!");
}

//while Loop
let i = 1;
while(i<=5) {
    console.log("i =", i);
    i++;
}
let j = 1;
while (j <= 3) {
    console.log("Raju")
    j++;
}

//do-while Loop
let k = 17;
do {
    console.log("Mr. Raju");
    k++;
} while (k <= 11);

let x = 1;
do {
    console.log("x =", x);
    x++;
} while (x <= 5);

