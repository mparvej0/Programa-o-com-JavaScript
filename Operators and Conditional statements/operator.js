//Arithmetic Operators
let a = 5;
let b = 2;
console.log("a + b =", a + b);
console.log("a - b =", a - b);
console.log("a * b =", a * b);
console.log("a / b =", a / b);
console.log("a % b =", a % b);
console.log("a ** b =", a ** b);
a++; //Or ++a
console.log("a =", a);
b--; //Or --b
console.log("b =", b);

